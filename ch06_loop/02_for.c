#include <stdio.h>

/*
for문
 - 반복 횟수를 아는 경우 사용 ex) 게시글 목록
 - for문은 (초기식, 조건식, 증감식)으로 구성
    - 초기식 : 최초 1회만 
    - 조건식 : 조건 해당하는 동안 반복
    - 증감식 : 
 - i, j, k 변수 → 반복 횟수
 - for 블럭문 내에서 i, j, k의 값을 수정 x
*/

int main(){

for(int i=1; i<10; i++){            // ++i였으면 후위(선실후+1)
    printf("2x%d=%d\n", i,i*2);     // 구구단단 2단 
}


}